    private void cargarEjercicios() {
        modeloTabla.setRowCount(0);
    for (Ejercicio ejercicio : controlador.obtenerEjercicios()) {
        modeloTabla.addRow(new Object[]{
            ejercicio.getNombre(),
            ejercicio.getGrupoMuscular()
        });
    }
}
    /**
 * Obtiene el ejercicio seleccionado en la tabla.
 *
 * @return Ejercicio seleccionado o null.
 */
    private Ejercicio obtenerEjercicioSeleccionado() {
        int fila = tblEjercicios.getSelectedRow();
        if (fila == -1) {
            return null;
        }
        String nombre = modeloTabla.getValueAt(fila, 0).toString();
        return controlador.buscarEjercicio(nombre);
}
