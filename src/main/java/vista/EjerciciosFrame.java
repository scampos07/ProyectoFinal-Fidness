    private void tblEjerciciosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEjerciciosMouseClicked
      Ejercicio ejercicio = obtenerEjercicioSeleccionado();
        if (ejercicio != null) {
            lblNombreEjercicio.setText(ejercicio.getNombre());
            lblGrupoMuscular.setText(
            ejercicio.getGrupoMuscular().toString());
            txtDescripcion.setText(
            ejercicio.getDescripcion());
        }
    }//GEN-LAST:event_tblEjerciciosMouseClicked

