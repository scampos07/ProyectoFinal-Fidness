    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
       int fila = tblDisponibles.getSelectedRow();
        if (fila == -1) {
            javax.swing.JOptionPane.showMessageDialog(
                this,
                "Seleccione un ejercicio.");
        return;
        }   
        Ejercicio ejercicio = ejerciciosDisponibles.get(fila);
        ejerciciosSeleccionados.add(ejercicio);
        ejerciciosDisponibles.remove(fila);
        cargarTablas();
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnQuitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuitarActionPerformed
        int fila = tblRutina.getSelectedRow();
        if (fila == -1) {
            javax.swing.JOptionPane.showMessageDialog(
                this,
                "Seleccione un ejercicio de la rutina.");
        return;
        }
        Ejercicio ejercicio = ejerciciosSeleccionados.get(fila);
        ejerciciosDisponibles.add(ejercicio);
        ejerciciosSeleccionados.remove(fila);
        cargarTablas();
    }//GEN-LAST:event_btnQuitarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombre = txtNombreRutina.getText().trim();
        if (nombre.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(
                this,
                "Ingrese el nombre de la rutina.");
        return;
        }
        if (ejerciciosSeleccionados.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(
                this,
                "Debe agregar al menos un ejercicio.");
        return;
        }
        NivelRutina nivel = NivelRutina.valueOf(
            cboNivel.getSelectedItem().toString());
        Rutina rutina = new Rutina(nombre, nivel);
        for (Ejercicio ejercicio : ejerciciosSeleccionados) {
            rutina.agregarEjercicio(ejercicio);
        }      
        rutinaController.agregarRutina(rutina);
        javax.swing.JOptionPane.showMessageDialog(
            this,
            "Rutina creada correctamente.");
        RutinasFrame rutinas = new RutinasFrame(usuario);
        rutinas.setVisible(true);
        dispose();
    }//GEN-LAST:event_btnGuardarActionPerformed

