    private void cargarNiveles() {
        cboNivel.removeAllItems();
        for (NivelRutina nivel : NivelRutina.values()) {
            cboNivel.addItem(nivel.name());
        }
    }

    private void cargarEjercicios() {
        modeloDisponibles.setRowCount(0);
        ejerciciosDisponibles.clear();
        ejerciciosDisponibles.addAll(
            ejercicioController.obtenerEjercicios());
        for (Ejercicio ejercicio : ejerciciosDisponibles) {
            modeloDisponibles.addRow(new Object[]{
            ejercicio.getNombre(),
            ejercicio.getGrupoMuscular()
            });
        }
    }
    private void cargarTablas() {
        modeloDisponibles.setRowCount(0);
        modeloRutina.setRowCount(0);
        for (Ejercicio ejercicio : ejerciciosDisponibles) {
            modeloDisponibles.addRow(new Object[]{
            ejercicio.getNombre(),
            ejercicio.getGrupoMuscular()
            });
        }
        for (Ejercicio ejercicio : ejerciciosSeleccionados) {  
            modeloRutina.addRow(new Object[]{
                ejercicio.getNombre(),
                ejercicio.getGrupoMuscular()
            });
        }
    }

